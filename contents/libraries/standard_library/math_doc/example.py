import math


def example_cos():
    print("Example cos")
    print(math.cos(0))
    print(math.cos(math.pi))
    print()


def example_sin():
    print("Example sin")
    print(math.sin(0))
    print(math.sin(math.pi))
    print()


def example_tan():
    print("Example tan")
    print(math.tan(0))
    print(math.tan(math.pi))
    print()



example_cos()
example_sin()
example_tan()

